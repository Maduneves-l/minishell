#include "libft.h"

long long int	ft_atoi(const char *str)
{
	int				sign;
	long long int	res;
	int				i;

	i = 0;
	res = 0;
	sign = 1;
	while ((str[i] == 32) || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		sign *= -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		res = res * 10;
		res = res + (str[i] - '0');
		i++;
	}
	return (sign * res);
}
